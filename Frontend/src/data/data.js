// ✅ FIND MATCHING SPECIALTIES
export function findMatchingSpecialties(userSymptoms) {
  const searchTerms = userSymptoms
    .toLowerCase()
    .split(/[\s,]+/)
    .filter((term) => term.length > 2);

  const matches = [];

  for (const category of symptomMapping) {
    let score = 0;

    for (const term of searchTerms) {
      for (const symptom of category.symptoms) {
        if (symptom.includes(term) || term.includes(symptom)) {
          score += 1;
        }
      }
    }

    if (score > 0) {
      matches.push({ category, score });
    }
  }

  matches.sort((a, b) => b.score - a.score);
  return matches.map((m) => m.category);
}

